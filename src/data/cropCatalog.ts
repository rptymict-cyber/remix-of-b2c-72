function sanitizeVarieties(cropName: string, raw: string[]): string[] {
  const seen = new Set<string>();
  const out: string[] = [];
  for (const v of raw) {
    const t = v.trim();
    if (!t) continue;
    if (t === "-" || t === "사용불가") continue;
    if (t === cropName) continue; // 중분류명과 동일 → 전체 품종으로 대체
    if (seen.has(t)) continue;
    seen.add(t);
    out.push(t);
  }
  return out;
}

function buildCatalog(): CropItem[] {
  const items: CropItem[] = [];
  let cIdx = 0;
  for (const category of CATEGORIES) {
    const block = RAW[category];
    let nIdx = 0;
    for (const [rawName, rawVars] of block) {
      // 대분류 placeholder 제거
      if (rawName === `${category}(일반)`) {
        nIdx++;
        continue;
      }
      const displayName = rawName === "기타" ? `${category} 기타` : rawName;
      const varieties = sanitizeVarieties(rawName, rawVars);
      const icon = CROP_EMOJI[rawName] || CATEGORY_EMOJI[category];
      items.push({
        id: `c${cIdx}-${nIdx}`,
        name: displayName,
        category,
        varieties,
        icon,
        isRepresentative: REPRESENTATIVE_NAMES.has(rawName),
      });
      nIdx++;
    }
    cIdx++;
  }
  return items;
}

export const ALL_CROPS: CropItem[] = buildCatalog();

export const REPRESENTATIVE_CROPS: CropItem[] = (() => {
  // 사용자 명세 순서대로 정렬
  const order = [
    "벼", "배추", "무", "마늘", "양파", "풋고추",
    "사과", "배", "감자", "고구마", "콩", "대파",
    "감귤", "복숭아", "수박", "딸기", "토마토", "상추",
  ];
  const map = new Map(ALL_CROPS.filter((c) => c.isRepresentative).map((c) => [c.name, c]));
  return order.map((n) => map.get(n)).filter((c): c is CropItem => !!c);
})();

export function findCropById(id: string): CropItem | undefined {
  return ALL_CROPS.find((c) => c.id === id);
}

/**
 * 작물명/품종명 어디든 매칭. 결과는 작물 단위, 작물명 매치 우선.
 */
export function searchCrops(q: string): CropItem[] {
  const k = q.trim();
  if (!k) return ALL_CROPS;
  const nameHit: CropItem[] = [];
  const varHit: CropItem[] = [];
  for (const c of ALL_CROPS) {
    if (c.name.includes(k)) {
      nameHit.push(c);
    } else if (c.varieties.some((v) => v.includes(k))) {
      varHit.push(c);
    }
  }
  return [...nameHit, ...varHit];
}

export function filterByCategory(category: CropCategory | "전체"): CropItem[] {
  if (category === "전체") return ALL_CROPS;
  return ALL_CROPS.filter((c) => c.category === category);
}